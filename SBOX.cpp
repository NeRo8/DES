#include "DES.h"

string DES::SBOX(string x) {
	string test = "";
	for (int i = 0, q = 0; i < 48; i += 6, q++) {
		string t = "";
		string a, b = "";
		int z, y = 0;

		for (int j = i; j < i + 6; j++) {
			t += x[j];
		}
		a = a + t[0] + t[5];
		b = b + t[1] + t[2] + t[3] + t[4];

		bitset<2>alf(a);
		bitset<4>blf(b);

		z = alf.to_ulong();
		y = blf.to_ulong();
		switch (q) {
		case 0:
			test += S1(z, y);
			break;
		case 1:
			test += S2(z, y);
			break;
		case 2:
			test += S3(z, y);
			break;
		case 3:
			test += S4(z, y);
			break;
		case 4:
			test += S5(z, y);
			break;
		case 5:
			test += S6(z, y);
			break;
		case 6:
			test += S7(z, y);
			break;
		case 7:
			test += S8(z, y);
			break;
		};
	}
	return test;
};

